package de.ms.productcatalogservices.model;

public class ProductCatalogSearchModel {
	
	private String productCatalogDescription;
	private String productCatalogTitle;
	
	public String getProductCatalogDescription() {
		return productCatalogDescription;
	}
	public void setProductCatalogDescription(String productCatalogDescription) {
		this.productCatalogDescription = productCatalogDescription;
	}
	public String getProductCatalogTitle() {
		return productCatalogTitle;
	}
	public void setProductCatalogTitle(String productCatalogTitle) {
		this.productCatalogTitle = productCatalogTitle;
	}	
}
